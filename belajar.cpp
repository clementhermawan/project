#include <iostream>
using namespace std;
int main () {

    int angka1 = 10;
    int angka2 = 3;

    int result;

    result = angka1 + angka2;

    cout<<"hasil penjumlahan + = "<<result<<endl;
    result = angka1 - angka2;
    cout<<"hasil pengurangan - = "<<result<<endl;
    result = angka1 * angka2;
    cout<<"hasil perkalian * = "<<result<<endl;
    result = angka1 / angka2;
    cout<<"hasil pembagian / = "<<result<<endl;
    result = angka1 % angka2;
    cout<<"hasil dari pembagian % = "<<result<<endl;



}
